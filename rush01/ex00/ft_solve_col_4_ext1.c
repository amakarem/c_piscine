int	ft_solve_col_4_ext1(int g_in[4][4], int g_out[4][4])
{
	int	col;

	col = 0;
	while (col < 4)
	{
		if (g_in[0][col] == 1 && g_in[1][col] == 3)
			g_out[1][col] = 3;
		else if (g_in[1][col] == 1 && g_in[0][col] == 3)
			g_out[2][col] = 3;
		col++;
	}
	return (0);
}
