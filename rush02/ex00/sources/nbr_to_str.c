char	*nbr_to_str(int nb)
{
	char	str[12];
	char	*out;
	int		i;
	int		x;

	i = 0;
	while (nb != 0)
	{
		str[i] = (nb % 10) + 48;
		nb = (nb / 10);
		i++;
	}
	str[i] = '\0';
	i--;
	x = 0;
	while (i >= 0)
	{
		out[x] = str[i];
		i--;
		x++;
	}
	out[x] = '\0';
	return (out);
}
